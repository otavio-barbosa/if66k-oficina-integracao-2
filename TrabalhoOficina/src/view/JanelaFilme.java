    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (inputNome.getText().equals("") || inputNome.getText() == null) {
            JOptionPane.showMessageDialog(null, "Preencha o nome!!!");
            return;
        }
        
        if (inputGenero.getText().equals("") || inputGenero.getText() == null) {
            JOptionPane.showMessageDialog(null, "Preencha o gênero!!!");
            return;
        }
        
        if (inputAno.getText().equals("") || inputAno.getText() == null) {
            JOptionPane.showMessageDialog(null, "Preencha o ano!!!");
            return;
        }

        try {
            filme.cadastrar(inputNome.getText(), inputGenero.getText(), Integer.valueOf(inputAno.getText())); 
            
            
            filme.ler();
        
            inputNome.setText("");
            inputGenero.setText("");
            inputAno.setText("");
        } catch(java.lang.NumberFormatException erro) {
            JOptionPane.showMessageDialog(null, "Erro: " + erro + "Verifique se o campo ano possui somente números inteiros!!!" );
        }

        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

        modelo.setRowCount(0);

        int posLin = 0;

        for (Filme film : filme.getArrayFilme()) {
            modelo.insertRow(posLin, new Object[]{film.getNome(), film.getGenero(), film.getAno(), posLin});

            posLin++;
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        String nome = inputNome.getText();
        String gene = inputGenero.getText();
        int ano = Integer.parseInt(inputAno.getText());
        filme.atualizar(nome, gene, ano, qual);
        
        inputNome.setText("");
        inputGenero.setText("");
        inputAno.setText("");
        
        filme.ler();
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

        modelo.setRowCount(0);

        int posLin = 0;

        for (Filme film : filme.getArrayFilme()) {
            modelo.insertRow(posLin, new Object[]{film.getNome(), film.getGenero(), film.getAno(), posLin});

            posLin++;
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

