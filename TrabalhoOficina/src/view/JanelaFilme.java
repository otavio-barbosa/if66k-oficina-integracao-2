    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        int teste = tabela.getSelectedRow();
        
        TableModel model = tabela.getModel();
        String testando1 = model.getValueAt(teste, 0).toString();
        String testando2 = model.getValueAt(teste, 1).toString();
        String testando3 = model.getValueAt(teste, 2).toString();
        
        qual = testando1;

        inputNome.setText(testando1);
        inputGenero.setText(testando2);
        inputAno.setText(testando3);
    }//GEN-LAST:event_tabelaMouseClicked

